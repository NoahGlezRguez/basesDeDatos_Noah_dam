CREATE DATABASE alumnado_dam COLLATE utf8mb4_spanish_ci;
USE alumnado_dam;

CREATE TABLE ALUMNOS
(
 DNI CHAR(9) PRIMARY KEY,
 NOMBRE VARCHAR(40) NOT NULL,
 EDAD INT UNSIGNED NOT NULL,
 LOCALIDAD VARCHAR(20) NOT NULL
);

CREATE TABLE ANTIGUOS
(
 DNI CHAR(9) PRIMARY KEY,
 NOMBRE VARCHAR(40) NOT NULL,
 EDAD INT UNSIGNED NOT NULL,
 LOCALIDAD VARCHAR(20) NOT NULL
);

CREATE TABLE NUEVOS
(
 DNI CHAR(9) PRIMARY KEY,
 NOMBRE VARCHAR(40) NOT NULL,
 EDAD INT UNSIGNED NOT NULL,
 LOCALIDAD VARCHAR(20) NOT NULL
);

